			<h1>What is responsible travel?</h1>
			<p>Responsible tourism includes many types of travel, all of which aim to minimize tourism's negative
				impacts on the environment, and maximize the positive contributions tourism can make to local
				communities. Traveling responsibly is not about making sacrifices, stopping development, or staying
				home. It is about designing tourism programs and individual trips carefully, to provide travelers with
				the experience they seek, while leaving a positive footprint at their destination.</p>
			<p>The Center for Responsible Travel (CREST) recognizes the need to improve the social and environmental
				impacts of all aspects of tourism, from small-scale ecotravel, to large-scale resort tourism. There are
				ways to improve the footprint of any style of travel. CREST's goal is to transform the travel industry
				as a whole into a driver of positive change for tourism destinations, travelers, and the global economy.
			</p>
			<p>Responsible travel has taken on global significance in the 30 years since the term "ecotourism" was first
				used, reminding the travel industry to keep in mind what kind of footprint is left on the destinations
				it visits. The UN even designated 2002 as the International Year of Ecotourism. Today nearly every
				country with national parks and protected areas is marketing some type of ecotourism or responsible
				tourism; lending and aid agencies are funneling hundreds of millions of dollars into projects that
				include eco-conscious travel; major environmental organizations are sponsoring responsible tourism
				projects and departments; and millions of travelers are going on tours with travel providers who market
				responsible travel options.</p>
			<p>While interest in eco-conscious and socially conscious tourism has never been greater, the field has
				frequently lacked intellectual rigor, as well as broad consensus on definitions. Most tourism (including
				ecotourism) programs in U.S. universities are located within management schools for recreation, tourism,
				restaurants and hotels, leisure studies, and parks. With several notable exceptions, these programs
				focus on vocational skills and business training. There are, at present, no institutions in the U.S.
				comparable to the Center for Responsible Travel.</p>
			<p>CREST views responsible tourism as a development and conservation tool that has the potential to address
				some of the most complex and compelling social and natural scientific issues of our times. Among the
				questions CREST addresses through its research and field projects are:</p>
			<ul>
				<li>How can responsible tourism help to empower local communities and alleviate rural poverty?</li>
				<li>How can responsible tourism truly contribute to the survival of endangered flora and fauna?</li>
				<li>How can responsible tourism facilitate cross-cultural learning, while diminishing the exploitation
					of "host populations"?</li>
				<li>What will ensure that tourism provides both enjoyment and education?</li>
				<li>How can we monitor and protect biodiversity in areas used for tourism?</li>
				<li>How can we create harmony, not hostility, between people and protected areas?</li>
				<li>How can we decrease the negative social and environmental impacts of tourism?</li>
				<li>How can we build businesses that are, at once, environmentally responsible, socially beneficial,
